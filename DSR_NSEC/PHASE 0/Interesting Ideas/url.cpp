using namespace std;

void solve()
{
    string s;

    getline(cin, s);

    int count = 0;

    for (int i = 0; i < s.size(); i++)
    {

        if (s[i] == '?')
        {

            count = i + 1;

            break;
        }
    }

    for (int i = count; i < s.size(); i++)
    {

        if (s[i] == '=')
        {

            cout << ':' << " ";

            i++;
        }

        if (s[i] == '&')
        {

            cout << '\n';
        }
        else
        {

            cout << s[i];
        }
    }
}

signed main()
{

    ios::sync_with_stdio(false);
    cin.tie(NULL);
    cout.tie(NULL);

    int t;
    cin >> t;
    // t=1;
    while (t--)
    {
        solve();
    }
    return 0;
}