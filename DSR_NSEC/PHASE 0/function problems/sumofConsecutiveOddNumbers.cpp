void solve()
{
    int x, y;
    cin >> x >> y;
    if (x > y)
    {
        swap(x, y);
    }
    int sum = 0;
    for (int i = x + 1; i <= y - 1; i++)
    {
        if (i % 2 != 0)
        {
            sum += i;
        }
    }
    cout << sum << endl;
}

signed main()
{

    ios::sync_with_stdio(false);
    cin.tie(NULL);
    cout.tie(NULL);

    int t;
    cin >> t;
    // t=1;
    while (t--)
    {
        solve();
    }
    return 0;
}