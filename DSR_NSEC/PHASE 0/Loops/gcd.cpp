using namespace std;

void solve()
{
    int a, b;
    cin >> a >> b;

    if (a < b)
        swap(a, b);

    while (b != 0)
    {
        int temp = b;
        b = a % b;
        a = temp;
    }
    cout << a << "\n";
}

