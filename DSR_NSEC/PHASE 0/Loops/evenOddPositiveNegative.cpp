using namespace std;

void solve()
{
    int n;
    cin >> n;
    int arr[n];
    for (int i = 0; i < n; i++)
    {
        cin >> arr[i];
    }
    int evenCnt = 0;
    int oddCnt = 0;
    int posCnt = 0;
    int negCnt = 0;

    for (int i = 0; i < n; i++)
    {
        if (arr[i] > 0)
        {
            posCnt++;
        }
        else if (arr[i] < 0)
        {
            negCnt++;
        }

        if (arr[i] % 2 == 0) // Check for even
        {
            evenCnt++;
        }
        else // Else it's odd
        {
            oddCnt++;
        }
    }

    cout << "Even: " << evenCnt << endl;
    cout << "Odd: " << oddCnt << endl;
    cout << "Positive: " << posCnt << endl;
    cout << "Negative: " << negCnt << endl;
}

signed main()
{
    ios::sync_with_stdio(false);
    cin.tie(NULL);
    int t;
    cin >> t;
    //  t=1;
    while (t--)
    {
        solve();
    }
    return 0;
}