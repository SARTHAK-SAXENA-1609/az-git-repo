using namespace std;

void solve(int i, int j, int n)
{ // i and j are iterators of rows and columns respectively
    // Your pattern logic here
    if (i + j >= n - 1 && i - j >= 1 - n)
    {
        cout << "#";
    }
    else
    {
        cout << " ";
    }
}

int main()
{
    ios::sync_with_stdio(false);
    cin.tie(NULL);
    int t = 1;
    // cin >> t;
    while (t--)
    {
        int n;
        cin >> n;
        for (int i = 0; i < n; i++)
        {
            for (int j = 0; j < 2 * n - 1; j++)
            {
                solve(i, j, n);
            }
            cout << endl; // the outer loop is iterating one row to the another hence, we need to add the endl to change the row
        }
    }
    return 0;
}