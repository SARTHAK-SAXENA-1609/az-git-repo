using namespace std;

void solve()
{
    int n;
    cin >> n;
    int cnt1 = 0;
    int cnt2 = 0;
    int arr[n][n];
    for (int i = 0; i < n; i++)
    {
        for (int j = 0; j < n; j++)
        {
            cin >> arr[i][j];
        }
    }
    for (int i = 0; i < n; i++)
    {
        for (int j = 0; j < n; j++)
        {
            if (i == j)
            {
                cnt1 += arr[i][j];
            }
            else if (i + j == n - 1)
            {
                cnt2 += arr[i][j];
            }
        }
    }
    int result = cnt1 - cnt2;
    cout << abs(result) << endl;
}

signed main()
{

    ios::sync_with_stdio(false);
    cin.tie(NULL);
    cout.tie(NULL);

    int t;
    cin >> t;
    while (t--)
    {
        solve();
    }
    return 0;
}